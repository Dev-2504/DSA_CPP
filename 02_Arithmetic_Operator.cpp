#include<iostream>
using namespace std;

main() {
    int a,b;
    cout << "Enter a First Value : ";
    cin >>a;
    cout << "Enter a Second Value : ";
    cin >>b;

    cout << "A + B = " << a + b <<endl;
    cout << "A - B = " << a - b <<endl;
    cout << "A * B = " << a * b <<endl;
    cout << "A / B = " << a / b <<endl;
    cout << "A % B = " << a % b <<endl;
}